package day021;

public class AssignmentOps {
    public static void main(String[] args) {
        //+,-,*,/,%
        //=,+=,-=,*=,/=,%=
        int k=3;
        System.out.println(k);
        k+=10;//        k=k+10;
        System.out.println(k);
        k-=5;// k=k-5;
        System.out.println(k);
        k*=2;//k=k*2;
        System.out.println(k);
        k/=4;//k=k/4;
        System.out.println(k);
        k%=3;//k=k%3;
        System.out.println(k);

        //Tanımlanan tamsayı bir değişkenin değerini 3 artıran java kodunu yazınız.
        // (Hem gelenksel hem de assignment olan hali)
    }
}
